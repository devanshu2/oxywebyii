                <h2>Ingredients</h2>
                <h3>For wrappers</h3>
                <ul>
                <li>15g desi ghee/refined oil</li>
                <li>1 cup maida (100g)</li>
                <li>1 ½ tbsp melted desi ghee/refined oil (15g)</li>
                <li>Salt to taste</li>
                <li>Enough water enough to make a stiff dough</li>
                </ul>
                
                <h3>For Filling</h3>
                <ul>
                <li>4-5 big potatoes boiled and mashed into pieces</li>
                <li>½ cup boiled peas</li>
                <li>Grated or finely chopped ginger</li>
                <li>2-3 green chillies-finely chopped</li>
                <li>1tsp jeera</li>
                <li>2 tsp coarsely crushed sabut dhania</li>
                <li>2 dry red chillies broken into pieces</li>
                <li>Salt to taste</li>
                <li>½ tsp amchur powder</li>
                <li>1 tsp red chilli power</li>
                <li>2-3 tbsp coriander</li>
                </ul>
                </div>
                <div class="recipe-right">
                 	<h2>How to Cook</h2>
                    <h3>Step 1- For wrappers</h3>
                    <p>Sieve maida and add salt &amp; melted ghee or oil. Rub to mix. Add just enough water to collect the dough and make stiff dough. Do not knead the dough too much. Keep aside covered for 20-30 min.</p>
                    
                    <h3>Step 2- For filling</h3>
                    <p>Heat a little olive oil in a pan, add jeera. Let it turn brown. Add coriander seeds and dry red chillies. Sauté for 30 sec. Add all the dry masalas in a bowl and add 1-2 tbsp water. Pour in pan. Add peas and boiled potatoes. Mix well and add coriander leaves. Remove from fire and cool.</p>
                    
                    <h3>Step 3</h3>
                    <p>Take marble size balls of the dough and roll into a thin chapatti. Cut into two halves. Apply water on edges. Fold like a cone. Fill some potato mixture. Close the top by applying some water. Keep aside.</p>
                    
                    
                    <h3>Step 4</h3>
                    <p>Preheat Oxy Fryer for 5 min. Arrange samosas in fry basket and close it properly. Keep the fryer at 140° C for 20 min and then at 180° C for 10-15 min to bring desired colour. Serve hot with green or tamarind chutney.</p>
